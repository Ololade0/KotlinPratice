fun main() {
    val number = 100
    val secondNumber = 10

    if(number == secondNumber){
        println("It is equal to eachother")
    }


    if(number != secondNumber){
        println("It is not equal  to eachother")
    }

    if(number >= secondNumber){
        println("It is greater than each other")
    }

    if(number <= secondNumber){
        println("It is lesser than each other")
    }

}

